package giis.demo.AsignacionInformes;

public class PeritosEntity {
	private String  idPerito;
	private String idColegiado;
	private String estado;
	
	public String getIdPerito() {
		return idPerito;
	}
	public void setIdPerito(String idPerito) {
		this.idPerito = idPerito;
	}
	public String getIdColegiado() {
		return idColegiado;
	}
	public void setIdColegiado(String idColegiado) {
		this.idColegiado = idColegiado;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	
	
}
