package giis.demo.colegiados;

public class ColegiadoDisplayDTO {
	private String idColegiado, nombre, apellidos;

	public String getIdColegiado() {
		return idColegiado;
	}

	public void setIdColegiado(String idColegiado) {
		this.idColegiado = idColegiado;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellidos() {
		return apellidos;
	}

	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
}
