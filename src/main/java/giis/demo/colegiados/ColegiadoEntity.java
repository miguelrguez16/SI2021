package giis.demo.colegiados;

public class ColegiadoEntity {
	private String idColegiado;
	private String nombre;
	private String apellidos;
	private String direccion;
	private String poblacion;
	private String telefono;
	private String datosBancarios;
	private String fechaSolicitudCOlegiado;
	private String titulacion;
	private String centro;
	private int anioTitulo;
	public String getIdColegiado() {
		return idColegiado;
	}
	public void setIdColegiado(String idColegiado) {
		this.idColegiado = idColegiado;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getPoblacion() {
		return poblacion;
	}
	public void setPoblacion(String poblacion) {
		this.poblacion = poblacion;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getDatosBancarios() {
		return datosBancarios;
	}
	public void setDatosBancarios(String datosBancarios) {
		this.datosBancarios = datosBancarios;
	}
	public String getFechaSolicitudCOlegiado() {
		return fechaSolicitudCOlegiado;
	}
	public void setFechaSolicitudCOlegiado(String fechaSolicitudCOlegiado) {
		this.fechaSolicitudCOlegiado = fechaSolicitudCOlegiado;
	}
	public String getTitulacion() {
		return titulacion;
	}
	public void setTitulacion(String titulacion) {
		this.titulacion = titulacion;
	}
	public String getCentro() {
		return centro;
	}
	public void setCentro(String centro) {
		this.centro = centro;
	}
	public int getAnioTitulo() {
		return anioTitulo;
	}
	public void setAnioTitulo(int anioTitulo) {
		this.anioTitulo = anioTitulo;
	}
	
	
	
}
